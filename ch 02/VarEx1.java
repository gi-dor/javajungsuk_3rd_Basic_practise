import java.time.Year;

public class VarEx1 {

	public static void main(String[] args) {
		
        
		int year = 0; 
	    int age =14;
	    
	    System.out.println(year);      // year = 0
	    System.out.println(age);       // age = 14
	    
	    year = age + 2000;      // 변수 age 값에 2000 더해 변수 year 저장
	    age = age + 1;          // 변수 age에 값에 +1 증가
	    
	    System.out.println(year);
	    System.out.println(age);
	    
		
		
	}

}
