package _ch05;
import java.util.Arrays;

public class Ex5_8 {

	public static void main(String[] args) {
	
	 int[] lotto = new int[45];      // 45개의 정수값 저장하기위해 배열생성
	 
	 // 배열의 각 요소에 1 ~ 45 값 저장
	 for (int i = 0; i < lotto.length; i++) 
		 lotto[i] = i +1;    // lotto[0]에 1이 저장
		 
		 int tmp = 0;  	  //두 값을 바꾸는데 쓸 임시변수
		 int j = 0;     	// 임의의 값을 얻어서 저장할 변수
		 
		 // 배열의 i번째 요소와 임의의 요소에 저장된 값을 서로 바꿔서 값을 섞는다
		 // 0 번째부터 5번째 요소 까지 모두 6개만 바꾼다
		 
		 for (int i = 0; i < 6; i++) {
			 j = (int)(Math.random() * 45);  // 0 ~ 44범위 임의의 값을 얻음
			 tmp = lotto [i];
			 lotto [i] = lotto [j];
			 lotto [j] = tmp;
		 }
		 
		 for (int i = 0; i <6; i++) {
			 System.out.printf("lotto[%d] = %d%n", i , lotto[i]);
	 }
	}
		

}
